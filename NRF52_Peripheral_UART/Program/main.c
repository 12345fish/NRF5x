#include "drivers\nrf5x_system.h"
#include "modules\module_serial.h"

#include "nrf5xdk_bsp.h"
/*====================================================================================================*/
/*====================================================================================================*/
void System_Init( void )
{
  Clock_Config();
  GPIO_Config();
  Serial_Config();

  printf("\fHello World!!\r\n");
}

int main( void )
{
  uint8_t recvByte = 0;

  System_Init();

  while(1) {
    LED1_Toggle();
    recvByte = Serial_RecvByte();
    Serial_SendByte(recvByte);
  }
}
