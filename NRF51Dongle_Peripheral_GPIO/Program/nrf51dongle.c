#include "drivers\nrf5x_system.h"
#include "nrf51dongle.h"
/*====================================================================================================*/
/*====================================================================================================*/
void NRF51_Init( void )
{
  NRF51_CKOCK_Config();
  NRF51_GPIO_Config();
}
/*====================================================================================================*/
/*====================================================================================================*/
void NRF51_Loop( void )
{
  while(1) {
    LEDB_Set();
    LEDR_Reset();
    delay_ms(200);
    LEDR_Set();
    LEDG_Reset();
    delay_ms(200);
    LEDG_Set();
    LEDB_Reset();
    delay_ms(200);
  }
}
