#include "drivers\nrf5x_system.h"
#include "nrf51dk.h"
/*====================================================================================================*/
/*====================================================================================================*/
void NRF51_Init( void )
{
  NRF51_CKOCK_Config();
  NRF51_GPIO_Config();
}
/*====================================================================================================*/
/*====================================================================================================*/
void NRF51_Loop( void )
{
  while(1) {
    LED3_Set();
    LED1_Reset();
    delay_ms(80);
    while(KEY1_Read()) {
      LED1_Toggle();
      delay_ms(80);
    }
    LED1_Set();
    LED2_Reset();
    delay_ms(80);
    while(KEY2_Read()) {
      LED2_Toggle();
      delay_ms(80);
    }
    LED2_Set();
    LED4_Reset();
    delay_ms(80);
    while(KEY4_Read()) {
      LED4_Toggle();
      delay_ms(80);
    }
    LED4_Set();
    LED3_Reset();
    delay_ms(80);
    while(KEY3_Read()) {
      LED3_Toggle();
      delay_ms(80);
    }
  }
}
