#include "drivers\nrf51_system.h"

#include "experiment_nrf51.h"
/*====================================================================================================*/
/*====================================================================================================*/
int main( void )
{
  GPIO_Config();

  while(1) {
    LED_3_Set();
    LED_1_Reset();
    Delay_10ms(8);
    while(KEY_1_Read()) {
      LED_1_Toggle();
      Delay_10ms(8);
    }
    LED_1_Set();
    LED_2_Reset();
    Delay_10ms(8);
    while(KEY_2_Read() == 0) {
      LED_2_Toggle();
      Delay_10ms(8);
    }
    LED_2_Set();
    LED_4_Reset();
    Delay_10ms(8);
    while(KEY_4_Read() == 0) {
      LED_4_Toggle();
      Delay_10ms(8);
    }
    LED_4_Set();
    LED_3_Reset();
    Delay_10ms(8);
    while(KEY_3_Read() == 0) {
      LED_3_Toggle();
      Delay_10ms(8);
    }
  }
}
/*====================================================================================================*/
/*====================================================================================================*/
void GPIO_Config( void )
{
  nrf_gpio_range_cfg_output(LED_1, LED_4);
  nrf_gpio_range_cfg_input(KEY_1, KEY_4, NRF_GPIO_PIN_PULLUP);
}
