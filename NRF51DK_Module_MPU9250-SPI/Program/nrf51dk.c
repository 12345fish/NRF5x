#include "nrf51dk.h"
/*====================================================================================================*/
/*====================================================================================================*/
void NRF51_Init( void )
{
  NRF51_CKOCK_Config();
  NRF51_GPIO_Config();
  NRF51_UART_Config();
  NRF51_MPU9250_Config();
}
/*====================================================================================================*/
/*====================================================================================================*/
void NRF51_Loop( void )
{
  int16_t imu[10] = {0};

  while(1) {
    LED3_Toggle();
    delay_ms(100);

    MPU9250_getData(imu);
    printf("AX:%5i\tAY:%5i\tAZ:%5i\tGX:%5i\tGY:%5i\tGZ:%5i\tMX:%5i\tMY:%5i\tMZ:%5i\r\n", imu[1], imu[2], imu[3], imu[4], imu[5], imu[6], imu[7], imu[8], imu[9]);
  }
}
