#include "drivers\nrf5x_system.h"
#include "modules\module_serial.h"

#include "nrf51dk.h"
/*====================================================================================================*/
/*====================================================================================================*/
void NRF51_Init( void )
{
  NRF51_CKOCK_Config();
  NRF51_GPIO_Config();
  NRF51_UART_Config();
}
/*====================================================================================================*/
/*====================================================================================================*/
void NRF51_Loop( void )
{
  uint8_t recvByte = 0;

  while(1) {
    recvByte = Serial_RecvByte();
    if(recvByte == 0x0D)
      Serial_SendByte(0x0A);
    Serial_SendByte(recvByte);
  }
}
