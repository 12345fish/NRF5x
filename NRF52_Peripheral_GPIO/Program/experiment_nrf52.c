/*====================================================================================================*/
/*====================================================================================================*/
#include "drivers\nrf52_system.h"
#include "experiment_nrf52.h"
/*====================================================================================================*/
/*====================================================================================================*/
void System_Init( void )
{
  nrf_drv_clock_init();
  GPIO_Config();
}

int main( void )
{
  System_Init();

  while(1) {
    LED3_Set();
    LED1_Reset();
    delay_ms(80);
    while(KEY1_Read()) {
      LED1_Toggle();
      delay_ms(80);
    }
    LED1_Set();
    LED2_Reset();
    delay_ms(80);
    while(KEY2_Read()) {
      LED2_Toggle();
      delay_ms(80);
    }
    LED2_Set();
    LED4_Reset();
    delay_ms(80);
    while(KEY4_Read()) {
      LED4_Toggle();
      delay_ms(80);
    }
    LED4_Set();
    LED3_Reset();
    delay_ms(80);
    while(KEY3_Read()) {
      LED3_Toggle();
      delay_ms(80);
    }
  }
}
/*====================================================================================================*/
/*====================================================================================================*/
void GPIO_Config( void )
{
  nrf_gpio_range_cfg_output(LED_1, LED_4);
  nrf_gpio_range_cfg_input(KEY_1, KEY_4, NRF_GPIO_PIN_PULLUP);
}
