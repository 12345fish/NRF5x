/*====================================================================================================*
**函數 : RNG_Config
**功能 : RNG Config
**輸入 : None
**輸出 : None
**使用 : RNG_Config();
**====================================================================================================*/
/*====================================================================================================*/
void RNG_Config( void )
{
  nrf_drv_rng_init(NULL);
}
/*====================================================================================================*/
/*====================================================================================================*
**函數 : RNG_vectGenerate
**功能 : Random Vector Generate
**輸入 : *randomBuf, lens
**輸出 : lens
**使用 : lens = RNG_vectGenerate(randomBuf, lens);
**====================================================================================================*/
/*====================================================================================================*/
uint8_t RNG_vectGenerate( uint8_t *randomBuf, uint8_t lens )
{
  uint8_t available = 0;

  nrf_drv_rng_bytes_available(&available);
  lens = (lens < available) ? lens : available;
  nrf_drv_rng_rand(randomBuf, lens);

  return lens;
}
