/*====================================================================================================*/
#include "nrf51_system.h"
#include "nrf51_temp.h"
#include "experiment_nrf51.h"
#include "module_rs232.h"
/*====================================================================================================*/
/*====================================================================================================*/
int main( void )
{
  int32_t Temp = 0;

  GPIO_Config();
  RS232_Config();

  Delay_1ms(1);

  RS232_SendStr((uint8_t*)"\r\nHello World!\r\n\r\n");

  while(1) {
    nrf_gpio_pin_toggle(LED_1);
    nrf_gpio_pin_toggle(LED_2);
    nrf_gpio_pin_toggle(LED_3);
    nrf_gpio_pin_toggle(LED_4);
    Delay_100ms(5);

    RS232_SendStr((uint8_t*)"Chip Temp : ");
    Temp = (TEMP_GetTemp()*25);
    RS232_SendNum(Type_D, 5, Temp);
    RS232_SendStr((uint8_t*)" deg\r\n");
  }
}
/*====================================================================================================*/
/*====================================================================================================*/
void GPIO_Config( void )
{
  nrf_gpio_range_cfg_output(LED_1, LED_4);
}
