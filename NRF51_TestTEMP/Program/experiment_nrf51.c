#include "nrf51_system.h"
#include "nrf51_temp.h"
#include "nrf51_delay.h"
#include "experiment_nrf51.h"
#include "module_rs232.h"
#include "algorithm_string.h"
/*====================================================================================================*/
/*====================================================================================================*/

int main( void )
{
  s32 Temp = 0;

  TEMP_Config();
  GPIO_Config();
  RS232_Config();

  Delay_1ms(1);

  while(1) {
    nrf_gpio_pin_toggle(18);
    nrf_gpio_pin_toggle(19);
    Delay_100ms(5);

    RS232_SendStr((u8*)"Chip Temp : ");
    Temp = (TEMP_GetTemp()*25);
    RS232_SendNum(Type_D, 5, Temp);
    RS232_SendStr((u8*)" deg\r\n");
  }
}
/*====================================================================================================*/
/*====================================================================================================*/
void GPIO_Config( void )
{
  nrf_gpio_range_cfg_output(18, 19);    // Pin 18-19 - output
}
