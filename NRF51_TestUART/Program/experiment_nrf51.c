/*====================================================================================================*/
/*====================================================================================================*/
#include "nrf51_system.h"
#include "nrf51_delay.h"
#include "experiment_nrf51.h"
#include "module_rs232.h"
/*====================================================================================================*/
/*====================================================================================================*/
int main( void )
{
  u8 RecvData = 0;

  GPIO_Config();
  RS232_Config();

  Delay_1ms(1);

  RS232_SendStr((u8*)"\r\nHellow World!\r\n\r\n");

  while(1) {
    nrf_gpio_pin_toggle(18);
    nrf_gpio_pin_toggle(19);

    if(RS232_RecvByteWTO(&RecvData, 1000) == SUCCESS) {
      if(RecvData == 0x0D)  // if press enter
        RS232_SendStr((u8*)"\r\n");
      else
        RS232_SendByte(RecvData);
    }
    else
      RS232_SendStr((u8*)"Timeout!\r\n");
  }
}
/*====================================================================================================*/
/*====================================================================================================*/
void GPIO_Config( void )
{
  nrf_gpio_range_cfg_output(18, 19);    // Pin 18-19 - output
}
