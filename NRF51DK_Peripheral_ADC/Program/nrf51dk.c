/*====================================================================================================*/
/*====================================================================================================*/
void NRF51_Init( void )
{
  NRF51_CKOCK_Config();
  NRF51_GPIO_Config();
  NRF51_ADC_Config();
  NRF51_UART_Config();
}
/*====================================================================================================*/
/*====================================================================================================*/
void NRF51_Loop( void )
{
  int16_t adc = 0;

  while(1) {
    LED1_Toggle();
    delay_ms(100);
    adc = ADC_Read(NRF_ADC_CONFIG_INPUT_7);
    printf("AD = %d\r\n", adc);
  }
}
